    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        Trabajador trabajador = new Trabajador();

        String nombre = jTextField1.getText();
        String apellidos = jTextField2.getText();
        String dni = jTextField3.getText();

        trabajador.setNombre( nombre.isBlank() ? null : nombre );
        trabajador.setApellidos( apellidos.isBlank() ? null : apellidos );
        trabajador.setDni( dni.isBlank() ? null : dni );

        try {

            trabajadorServicio.guardar(trabajador);
            trabajadorServicio.llenarTabla(jTable1);

            limpiarFormulario();

            JOptionPane.showMessageDialog(
                    this,
                    "Trabajador registrado",
                    "Información",
                    JOptionPane.INFORMATION_MESSAGE);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(
                    this,
                    "No se pudo registrar el trabajador.\n\n"+ 
                    "1- El dni debe ser único\n"+ 
                    "2- Todos los campos son obligatorios",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_jButton3ActionPerformed

