    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        int selectedRow = jTable1.getSelectedRow();
        int selectedColumn = jTable1.getSelectedColumn();

        Object value = jTable1.getValueAt(selectedRow, selectedColumn);
        long id = (long) jTable1.getValueAt(selectedRow, 0);

        if (value instanceof JButton) {

            if (selectedColumn == 4) {

                Trabajador trabajador = trabajadorServicio.obtener(id);

                modificarTrabajador.setTrabajador(trabajador);
                modificarTrabajador.setTabla(jTable1);
                modificarTrabajador.setModal(true);
                modificarTrabajador.setLocationRelativeTo(this);
                modificarTrabajador.setVisible(true);

            }

            if (selectedColumn == 5) {
                int opcion = JOptionPane.showConfirmDialog(
                        this,
                        "¿Está seguro que quiere eliminar este trabajador?",
                        "Información",
                        JOptionPane.YES_NO_OPTION,
                        JOptionPane.QUESTION_MESSAGE);

                if (opcion == JOptionPane.YES_OPTION) {

                    trabajadorServicio.borrar(id);

                    trabajadorServicio.llenarTabla(jTable1);

                    JOptionPane.showMessageDialog(
                            this,
                            "Registro eliminado correctamente",
                            "Información",
                            JOptionPane.INFORMATION_MESSAGE);

                }
            }

        }
    }//GEN-LAST:event_jTable1MouseClicked

